SELECT
    CASE
        WHEN "RELATIVE_VELOCITY_KMH" < 5000 THEN '< 5,000 km/h'
        WHEN "RELATIVE_VELOCITY_KMH" BETWEEN 5000 AND 10000 THEN '5,000–10,000 km/h'
        WHEN "RELATIVE_VELOCITY_KMH" BETWEEN 10000 AND 20000 THEN '10,000–20,000 km/h'
        WHEN "RELATIVE_VELOCITY_KMH" BETWEEN 20000 AND 30000 THEN '20,000–30,000 km/h'
        ELSE '> 30,000 km/h'
    END AS velocity_check,
    COUNT(*) AS asteroid_count
FROM nasa.raw.nasa_neo_table
GROUP BY 1
ORDER BY 2